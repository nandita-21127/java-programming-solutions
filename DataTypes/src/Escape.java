public class Escape {
    public static void main(String[] args) {
        System.out.println("hello Good morning Kg coding");
        System.out.println("hello \'Good morning\' Kg coding");
        System.out.println("hello \" Good morning\" Kg coding");
        System.out.println("hello \n Good morning \t Kg coding");
        System.out.println("hello \t Good morning \b Kg coding");
        System.out.println("hello \\Good morning Kg \\ coding");
    }
}
